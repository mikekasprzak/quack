#ifndef __Geometry_BGRASet_H__
#define __Geometry_BGRASet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class BGRASet {
public:
	T b, g, r, a;

public:
	BGRASet() {
	}

	BGRASet( T _b, T _g, T _r, T _a ) :
		b( _b ),
		g( _g ),
		r( _r ),
		a( _a )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const BGRASet& _Vs ) const {
		if ( (b == _Vs.b) )
			if ( (g == _Vs.g) )
				if ( (r == _Vs.r) )
					return (a == _Vs.a);
		return false;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline T& operator [] ( const int Index ) {
		return *((&b)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const T& operator [] ( const int Index ) const {
		return *((&b)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_BGRASet_H__ //
// - ------------------------------------------------------------------------------------------ - //

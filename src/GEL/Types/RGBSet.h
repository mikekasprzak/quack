#ifndef __Geometry_RGBSet_H__
#define __Geometry_RGBSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class RGBSet {
public:
	T r, g, b;

public:
	RGBSet() {
	}
	
	RGBSet( T _r, T _g, T _b ) :
		r( _r ),
		g( _g ),
		b( _b )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const RGBSet& _Vs ) const {
		if ( (r == _Vs.r) )
			if ( (g == _Vs.g) )
			return (b == _Vs.b);
		return false;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline T& operator [] ( const int Index ) {
		return *((&r)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const T& operator [] ( const int Index ) const {
		return *((&r)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_RGBSet_H__ //
// - ------------------------------------------------------------------------------------------ - //

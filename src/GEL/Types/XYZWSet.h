#ifndef __Geometry_XYZWSet_H__
#define __Geometry_XYZWSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class XYZWSet {
public:
	T x, y, z, w;

public:
	XYZWSet() {
	}

	XYZWSet( T _x, T _y, T _z, T _w ) :
		x( _x ),
		y( _y ),
		z( _z ),
		w( _w )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const XYZWSet& _Vs ) const {
		if ( (x == _Vs.x) )
			if ( (y == _Vs.y) )
				if ( (z == _Vs.z) )
					return (w == _Vs.w);
		return false;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline T& operator [] ( const int Index ) {
		return *((&x)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const T& operator [] ( const int Index ) const {
		return *((&x)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //	
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_XYZWSet_H__ //
// - ------------------------------------------------------------------------------------------ - //

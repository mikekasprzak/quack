#ifndef __Geometry_UVSet_H__
#define __Geometry_UVSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class UVSet {
public:
	T u, v;

public:
	UVSet() {
	}

	UVSet( T _u, T _v ) :
		u( _u ),
		v( _v )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const UVSet& _Vs ) const {
		if ( (u == _Vs.u) )
			return (v == _Vs.v);
		return false;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline T& operator [] ( const int Index ) {
		return *((&u)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const T& operator [] ( const int Index ) const {
		return *((&u)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_UVSet_H__ //
// - ------------------------------------------------------------------------------------------ - //

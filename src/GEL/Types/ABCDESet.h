#ifndef __Geometry_ABCDESet_H__
#define __Geometry_ABCDESet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class ABCDESet {
public:
	T a, b, c, d, e;

public:
	ABCDESet() {
	}

	ABCDESet( T _a, T _b, T _c, T _d, T _e ) :
		a( _a ),
		b( _b ),
		c( _c ),
		d( _d ),
		e( _e )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const ABCDESet& _Vs ) const {
		if ( (a == _Vs.a) )
			if ( (b == _Vs.b) )
				if ( (c == _Vs.c) )
					if ( (d == _Vs.d) )
						return (e == _Vs.e);
		return false;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline T& operator [] ( const int Index ) {
		return *((&a)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const T& operator [] ( const int Index ) const {
		return *((&a)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_ABCDESet_H__ //
// - ------------------------------------------------------------------------------------------ - //

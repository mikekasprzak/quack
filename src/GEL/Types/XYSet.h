#ifndef __Geometry_XYSet_H__
#define __Geometry_XYSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class XYSet {
public:
	T x, y;

public:
	XYSet() {
	}

	XYSet( T _x, T _y ) :
		x( _x ),
		y( _y )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const XYSet& _Vs ) const {
		if ( (x == _Vs.x) )
			return (y == _Vs.y);
		return false;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline T& operator [] ( const int Index ) {
		return *((&x)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const T& operator [] ( const int Index ) const {
		return *((&x)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_XYSet_H__ //
// - ------------------------------------------------------------------------------------------ - //

#ifndef __Geometry_XSet_H__
#define __Geometry_XSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class XSet {
public:
	T x;

public:
	XSet() {
	}

	XSet( T _x ) :
		x( _x )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const XSet& _Vs ) const {
		return (x == _Vs.x);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline T& operator [] ( const int Index ) {
		return *((&x)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const T& operator [] ( const int Index ) const {
		return *((&x)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_XSet_H__ //
// - ------------------------------------------------------------------------------------------ - //

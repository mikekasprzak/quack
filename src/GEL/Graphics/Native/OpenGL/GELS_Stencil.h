// - ------------------------------------------------------------------------------------------ - //
#if defined(USES_OPENGL) || defined(USES_OPENGLES)
// - ------------------------------------------------------------------------------------------ - //
#ifndef __GEL_Graphics_OpenGL_Stencil_H__
#define __GEL_Graphics_OpenGL_Stencil_H__
// - ------------------------------------------------------------------------------------------ - //
#include "API.h"
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#define GELS_SET_STENCIL_MASK
// - ------------------------------------------------------------------------------------------ - //
inline void gels_SetStencilMask( const int Mask = 0 ) {
	glStencilMask( Mask );
}
// - ------------------------------------------------------------------------------------------ - //


// - ------------------------------------------------------------------------------------------ - //
// Enable or Disable the Stencil Test (i.e. actually using the Stencil Buffer) //
// - ------------------------------------------------------------------------------------------ - //
#define GELS_ENABLE_STENCIL_TEST
// - ------------------------------------------------------------------------------------------ - //
inline void gels_EnableStencilTest() {
	glEnable( GL_STENCIL_TEST );
}
// - ------------------------------------------------------------------------------------------ - //
#define GELS_DISABLE_STENCIL_TEST
// - ------------------------------------------------------------------------------------------ - //
inline void gels_DisableStencilTest() {
	glDisable( GL_STENCIL_TEST );
}
// - ------------------------------------------------------------------------------------------ - //


// - ------------------------------------------------------------------------------------------ - //
#define GELS_SET_STENCIL_FUNC
// - ------------------------------------------------------------------------------------------ - //
inline void gels_SetStencilFunc( const int Func, const int Variable, const int Mask ) {
	glStencilFunc( Func, Variable, Mask );
}
// - ------------------------------------------------------------------------------------------ - //
#define GELS_SET_STENCIL_OP
// - ------------------------------------------------------------------------------------------ - //
inline void gels_SetStencilOp( const int Fail, const int zFail, const int zPass ) {
	glStencilOp( Fail, zFail, zPass );
}
// - ------------------------------------------------------------------------------------------ - //


// - ------------------------------------------------------------------------------------------ - //
#endif // __GEL_Graphics_OpenGL_Stencil_H__ //
// - ------------------------------------------------------------------------------------------ - //
#endif // USES //

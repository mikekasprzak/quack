// - ------------------------------------------------------------------------------------------ - //
#if defined(USES_OPENGL) || defined(USES_OPENGLES)
// - ------------------------------------------------------------------------------------------ - //
#ifndef __GEL_Graphics_OpenGL_Depth_H__
#define __GEL_Graphics_OpenGL_Depth_H__
// - ------------------------------------------------------------------------------------------ - //
#include "API.h"
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#define GELS_ENABLE_DEPTH_TEST
// - ------------------------------------------------------------------------------------------ - //
inline void gels_EnableDepthTest() {
	glEnable( GL_DEPTH_TEST );
}
// - ------------------------------------------------------------------------------------------ - //
#define GELS_DISABLE_DEPTH_TEST
// - ------------------------------------------------------------------------------------------ - //
inline void gels_DisableDepthTest() {
	glDisable( GL_DEPTH_TEST );
}
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#define GELS_ENABLE_DEPTH_WRITING
// - ------------------------------------------------------------------------------------------ - //
inline void gels_EnableDepthWriting() {
	glDepthMask( GL_TRUE );
}
// - ------------------------------------------------------------------------------------------ - //
#define GELS_DISABLE_DEPTH_WRITING
// - ------------------------------------------------------------------------------------------ - //
inline void gels_DisableDepthWriting() {
	glDepthMask( GL_FALSE );
}
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#define GELS_SET_DEPTH_FUNC
// - ------------------------------------------------------------------------------------------ - //
inline void gels_SetDepthFunc( const int Func ) {
	glDepthFunc( Func );
}
// - ------------------------------------------------------------------------------------------ - //
#define GELS_SET_DEPTH_RANGE
// - ------------------------------------------------------------------------------------------ - //
inline void gels_SetDepthRange( const Real zNear, const Real zFar ) {
#ifdef USES_OPENGLES
	glDepthRangef( zNear, zFar );
#else // USES_OPENGLES //
	glDepthRange( zNear, zFar );
#endif // USES_OPENGLES //
}
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __GEL_Graphics_OpenGL_Depth_H__ //
// - ------------------------------------------------------------------------------------------ - //
#endif // USES //
// - ------------------------------------------------------------------------------------------ - //

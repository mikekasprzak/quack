namespace Gel {
// - ------------------------------------------------------------------------------------------ - //
GelScreens Screen;
float ScreenScalar = 1.0f;
// - ------------------------------------------------------------------------------------------ - //
}; // namespace Gel //
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
void gelSetScreenScalar( const float _Scalar ) {
	Log( "Gel::ScreenScalar set to %.f%% (%f)", _Scalar*100.0f, _Scalar );
	Gel::ScreenScalar = _Scalar;
}
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
void gelInitScreen( const st Index ) {
	gelsInitScreen( Index );	// *** gels *** //
}
// - ------------------------------------------------------------------------------------------ - //
void gelInitScreens() {
	for ( st idx = 0; idx < Gel::Screen.Count(); idx++ ) {
		if ( Gel::Screen[idx].IsRequired() ) {
			gelInitScreen( idx );
		}
	}
}
// - ------------------------------------------------------------------------------------------ - //
void gelInitAllScreens() {
	for ( st idx = 0; idx < Gel::Screen.Count(); idx++ ) {
		gelInitScreen( idx );
	}
}
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
void gelSwapScreen( const st Index ) {
	gelsSwapScreen( Index );	// *** gels *** //
}
// - ------------------------------------------------------------------------------------------ - //
void gelSwapScreens() {
	for ( st idx = 0; idx < Gel::Screen.Count(); idx++ ) {
		gelSwapScreen( idx );
	}
}

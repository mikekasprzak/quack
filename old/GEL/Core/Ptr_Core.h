#ifndef __GEL2_CORE_PTR_CORE_H__
#define __GEL2_CORE_PTR_CORE_H__
// - ------------------------------------------------------------------------------------------ - //
#include <string.h>
// - ------------------------------------------------------------------------------------------ - //
inline const bool is_2_byte_aligned_Ptr( void* Ptr ) {
	return ((size_t)Ptr & 0x1) == 0;
}
// - ------------------------------------------------------------------------------------------ - //
inline const bool is_4_byte_aligned_Ptr( void* Ptr ) {
	return ((size_t)Ptr & 0x3) == 0;
}
// - ------------------------------------------------------------------------------------------ - //
inline const bool is_8_byte_aligned_Ptr( void* Ptr ) {
	return ((size_t)Ptr & 0x7) == 0;
}
// - ------------------------------------------------------------------------------------------ - //
inline const bool is_16_byte_aligned_Ptr( void* Ptr ) {
	return ((size_t)Ptr & 0xF) == 0;
}
// - ------------------------------------------------------------------------------------------ - //
inline const bool is_32_byte_aligned_Ptr( void* Ptr ) {
	return ((size_t)Ptr & 0x1F) == 0;
}
// - ------------------------------------------------------------------------------------------ - //
inline const bool is_64_byte_aligned_Ptr( void* Ptr ) {
	return ((size_t)Ptr & 0x3F) == 0;
}
// - ------------------------------------------------------------------------------------------ - //
inline const bool is_128_byte_aligned_Ptr( void* Ptr ) {
	return ((size_t)Ptr & 0x7F) == 0;
}
// - ------------------------------------------------------------------------------------------ - //
inline const bool is_256_byte_aligned_Ptr( void* Ptr ) {
	return ((size_t)Ptr & 0xFF) == 0;
}
// - ------------------------------------------------------------------------------------------ - //
inline const bool is_aligned_Ptr( void* Ptr ) {
	return is_4_byte_aligned_Ptr( Ptr );
}
// - ------------------------------------------------------------------------------------------ - //
#endif // __GEL2_CORE_PTR_CORE_H__ //

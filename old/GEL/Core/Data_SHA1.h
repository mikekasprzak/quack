// Hash a block of data with an SHA1 sum //
// - ------------------------------------------------------------------------------------------ - //
inline const SHA1Hash hash_SHA1_Data( const void* _Src, const size_t _Size ) {
	SHA1Hash Key;
	
	sha1_ctx HashInfo;
	sha1_init_ctx( &HashInfo );
	sha1_process_bytes( _Src, _Size, &HashInfo );
	sha1_finish_ctx( &HashInfo, (void*)&Key.Hash );
	
	return Key;
}

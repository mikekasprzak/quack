#ifndef __Geometry_UVWSet_H__
#define __Geometry_UVWSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class UVWSet {
public:
	T u, v, w;

public:
	UVWSet() {
	}

	UVWSet( T _u, T _v, T _w ) :
		u( _u ),
		v( _v ),
		w( _w )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const UVWSet& _Vs ) const {
		if ( (u == _Vs.u) )
			if ( (v == _Vs.v) )
				return (w == _Vs.w);
		return false;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline T& operator [] ( const int Index ) {
		return *((&u)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const T& operator [] ( const int Index ) const {
		return *((&u)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_UVSet_H__ //
// - ------------------------------------------------------------------------------------------ - //

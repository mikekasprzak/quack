#ifndef __Geometry_BGRSet_H__
#define __Geometry_BGRSet_H__
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
template <class T>
class BGRSet {
public:
	T b, g, r;

public:
	BGRSet() {
	}
	
	BGRSet( T _b, T _g, T _r ) :
		b( _b ),
		g( _g ),
		r( _r )
	{
	}
	
	// - -------------------------------------------------------------------------------------- - //
	inline bool operator == ( const BGRSet& _Vs ) const {
		if ( (b == _Vs.b) )
			if ( (g == _Vs.g) )
				return (r == _Vs.r);
		return false;
	}
	// - -------------------------------------------------------------------------------------- - //
	inline T& operator [] ( const int Index ) {
		return *((&b)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
	inline const T& operator [] ( const int Index ) const {
		return *((&b)+Index);
	}
	// - -------------------------------------------------------------------------------------- - //
};
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
#endif // __Geometry_BGRSet_H__ //
// - ------------------------------------------------------------------------------------------ - //
